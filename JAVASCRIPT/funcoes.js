
function somaChocolate(){
        var val1 = parseInt(document.getElementById("chocolate1").value);
        var val2 = parseInt(document.getElementById("chocolate2").value);
        var val3 = parseInt(document.getElementById("chocolate3").value);
        var val4 = parseInt(document.getElementById("chocolate4").value);
        


        var total = ((val1*15)+(val2*25) + (val3*5)+(val4*3));

        
        document.getElementById('tela').setAttribute("value", total);

      
    }

